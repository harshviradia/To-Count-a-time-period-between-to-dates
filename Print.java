public class Print {

    public Print(int fy, int fm, int fd, int fh, int FM, int fs) {

        System.out.print("Total time : ");
        System.out.print(fy);
        System.out.print("years ");
        System.out.print(fm);
        System.out.print("months ");
        System.out.print(fd);
        System.out.print("days ");
        System.out.print(fh);
        System.out.print("hours ");
        System.out.print(FM);
        System.out.print("mins ");
        System.out.print(fs);
        System.out.print("secs");
    }
}
